package com.srini.app;

public class StudentDao {
	private int sidd;
	private String sname;
	private int smarks;


	
	public int getSidd() {
		return sidd;
	}

	public void setSidd(int sidd) {
		this.sidd = sidd;
	}

	public String getSname() {
		return sname;
	}

	public void setSname(String sname) {
		this.sname = sname;
	}

	public int getSmarks() {
		return smarks;
	}

	public void setSmarks(int smarks) {
		this.smarks = smarks;
	}

}
